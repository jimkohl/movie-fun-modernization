package org.superbiz.moviefun.moviesapi.movies;

import java.util.Objects;

public class MovieInfo {
    
    private Long id;

    private String director;
    private String title;
    private Integer year;
    private String genre;
    private Integer rating;

    public MovieInfo() {
    }

    public MovieInfo(String title, String director, String genre, Integer rating, Integer year) {
        this.director = director;
        this.title = title;
        this.year = year;
        this.genre = genre;
        this.rating = rating;
    }

    public MovieInfo(String director, String title, Integer year) {
        this.director = director;
        this.title = title;
        this.year = year;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getDirector() {
        return director;
    }

    public void setDirector(String director) {
        this.director = director;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }

    public Integer getRating() {
        return rating;
    }

    public void setRating(Integer rating) {
        this.rating = rating;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        MovieInfo movieInfo = (MovieInfo) o;
        return Objects.equals(getId(), movieInfo.getId()) &&
                Objects.equals(getDirector(), movieInfo.getDirector()) &&
                Objects.equals(getTitle(), movieInfo.getTitle()) &&
                Objects.equals(getYear(), movieInfo.getYear()) &&
                Objects.equals(getGenre(), movieInfo.getGenre()) &&
                Objects.equals(getRating(), movieInfo.getRating());
    }

    @Override
    public int hashCode() {
        return Objects.hash(getId());
    }
}
